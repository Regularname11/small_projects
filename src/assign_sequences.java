
class Sequences {
    public static void main(String[] args) {
        //using backslash \ to print double quotes. 
        String mesg1 = "C:\\windows\\...";
        String mesg2 = "C:\nwindows\\...";
        String mesg3 = "Hi it's me \"taha\"";
        System.out.println(mesg1); //Hi it's me "taha"
        System.out.println(mesg2);
        System.out.println(mesg3);
    }
}